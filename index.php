				<h3>Games Are Account Based</h3>
					<p>One of my favorite features of <em>Steam</em> is how the games are all tied to your account.
					So if you have to build a new computer, you still have access to everything you purchased.
					Just install the <em>Steam</em> app and re-install your games.
					Of course if you lose your login information, it makes it more difficult to retrieve your titles. 
